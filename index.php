                  <li>Pembinaan teknis penyelenggaraan fungsi-fungsi penunjang keuangan.</li>
                  <li>Pelayanan dan pembinaan administrasi keuangan di lingkungan kota.</li>
                  <li>Penyelenggaraan dan pengelolaan administrasi dan urusan rumah tangga badan.</li>
                  <li>Pengendalian, pengawasan dan pembinaan di bidang administrasi kepegawaian, kearsipan,
                    ketatalaksanaan, ketatusahaan, pengelolaan anggaran, perlengkapan, kehumasan, dan
                    pelaksanaan tugas badan.</li>
                  <li>Pengembangan kemampuan organisasi meliputi pembinaan personil, administrasi umum,
