                    Badan.</li>
                  <li>Perumusan pengukuran kepuasan pengguna layanan.</li>
                  <li>Pengelolaan pengaduan masyarakat dibidang keuangan.</li>
                  <li>Penyampaian data hasil pembangunan dan informasi lainnya terkait layanan bidang keuangan
                    secara berkala melalui sub domain website Pemerintah Daerah.</li>
                  <li>Pelaksanaan pemantauan, evaluasi dan laporan pelaksanaan tugas bidang keuangan.</li>
                  <li>Pelaksanaan tugas kedinasan lain yang diberikan oleh Walikota sesuai dengan bidang
                    tugasnya.</li>
